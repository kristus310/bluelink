import java.awt.*;

public class ColorPallete {
    //Really dark gray - almost blackish
    public Color darkest() {
        return new Color(18, 18, 18);
    }

    //Dark gray
    public Color dark() {
        return new Color(68, 68, 68);
    }

    //Light gray
    public Color light() {
        return new Color(176, 176, 176);
    }

    //Really light gray - almost whitish
    public Color lightest() {
        return new Color(224, 224, 224);
    }
}
